export class SpurClient {
    constructor(token, timeout = 5000) {
        const headers = { "Token": token };
        const baseURL = "https://api.spur.us/v2";

        this.client = axios.create({ baseURL, timeout, headers });
    }

    async context(ip) {
        return this.client.get(`context/${ip}`).then((resp) => resp.data);
    }

    async metadata(tag) {
        return this.client.get(`metadata/tags/${tag}`).then((resp) => resp.data);
    }
}
